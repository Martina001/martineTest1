    class Solution {
        public int minSubArrayLen(int target, int[] nums) {

            int left = 0, right = 0;
            int n = nums.length;
            int len = n + 1;
            int sum = 0;
            while (right < n) {
                int num = nums[right];
                right++;
                sum += num;
                while (sum >= target) {
                    if (len > right - left) {
                        len = right - left;
                    }
                    sum -= nums[left];
                    left++;
                }
            }
            if (len == n + 1) return 0;
            else return len;
        }
    }
